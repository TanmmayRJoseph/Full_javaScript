//-----------------Switch Case-----------------

const month = "march";

switch (month) {
  case "jan":
    console.log("January");
    break;
  case "feb":
    console.log("February");
    break;
  case "march":
    console.log("March");
    break;
  case "april":
    console.log("April");
    break;
  case "may":
    console.log("May");
    break;
  default:
    console.log("Not a month");
    break;
}
